-- =============================================
-- Author:		Paul Stauskas
-- Create date: July 30, 2018
-- Description:	Generates a SQL script that will copy all records from a table
--              in a source database and put them into the destination database.
--              Creates appropriate code for tables that contain IDENTITY column.
-- Usage:	SET @script = 'uf_CreateInsertScript('mytable','DBName')
-- =============================================
CREATE FUNCTION [dbo].[uf_CreateInsertScript]
(
	@table_name SYSNAME,
	@database_name SYSNAME
	
)
RETURNS NVARCHAR(4000)
BEGIN

	DECLARE @uses_identity as bit
	SET @uses_identity = 0
	IF (OBJECTPROPERTY(OBJECT_ID(@table_name), 'TableHasIdentity') = 1) 
	BEGIN
		SET @uses_identity = 1
	END

	DECLARE @TO_DB nvarchar(200)
	SET @TO_DB = @database_name

	DECLARE @TableName nvarchar(200)
	SET @TableName = @table_name

	DECLARE @SynonymName nvarchar(200)
	SET @SynonymName =  'TO_' + @TableName

	DECLARE @SQL_SCRIPT nvarchar(4000)
	SET @SQL_SCRIPT = 'DROP SYNONYM IF EXISTS ' + @SynonymName + CHAR(13) + CHAR(10)
	SET @SQL_SCRIPT = @SQL_SCRIPT +'CREATE SYNONYM ' + @SynonymName + ' FOR ' +  @TO_DB + @TableName + CHAR(13) + CHAR(10) 
	SET @SQL_SCRIPT = @SQL_SCRIPT + 'IF NOT EXISTS (SELECT 1 FROM ' +  @SynonymName + ')' + CHAR(13) + CHAR(10) 
	SET @SQL_SCRIPT = @SQL_SCRIPT + 'BEGIN ' +  + CHAR(13) + CHAR(10)  + CHAR(9) + 'PRINT ''' + @database_name + @table_name + ' being populated ''' + CHAR(13) + CHAR(10)


	DECLARE @ColumnNames NVARCHAR(4000)
	SET @ColumnNames =  [dbo].uf_GetTableColumnNames(@TableName) 

	IF @uses_identity = 1
	BEGIN
		SET @SQL_SCRIPT =  @SQL_SCRIPT + 'SET IDENTITY_INSERT ' +  @TO_DB + @TableName + ' ON' + CHAR(13) + CHAR(10)
	END

	SET @SQL_SCRIPT =  @SQL_SCRIPT + 'INSERT INTO ' + @SynonymName + '(' + @ColumnNames + ') ' + CHAR(13) + CHAR(10)
	+ 'SELECT ' + @ColumnNames + ' FROM ' + @TableName + CHAR(13) + CHAR(10)

	IF @uses_identity = 1
	BEGIN
		SET @SQL_SCRIPT =  @SQL_SCRIPT + 'SET IDENTITY_INSERT ' +  @TO_DB + @TableName + ' OFF ' + CHAR(13) + CHAR(10)
	END

	SET @SQL_SCRIPT = @SQL_SCRIPT + 'END' + CHAR(13) + CHAR(10) + 'ELSE' +  CHAR(13) + CHAR(10) + 'BEGIN' + CHAR(13) + CHAR(10) + CHAR(9) + 'PRINT ''' + @database_name + @table_name + ' previously populated''' + CHAR(13) + CHAR(10) + 'END' +  CHAR(13) + CHAR(10)
	

    SET @SQL_SCRIPT =  @SQL_SCRIPT + 'DROP SYNONYM ' + @SynonymName	

	RETURN @SQL_SCRIPT

END
