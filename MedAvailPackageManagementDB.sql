/*
  Example of use:	

  DECLARE @shape int = 2
  DECLARE @length int = 132
  DECLARE @width int = 80
  DECLARE @height int = 66
  DECLARE @changed_by nvarchar(max) = 'bob'
  EXEC CreateContainer @shape,@length,@width,@height,@changed_by
*/
CREATE PROCEDURE [dbo].[CreateContainer]
(
            @shape int					    -- 0 = UnknownShape, 1 = Round, 2 = NotRound
           ,@length decimal(18,0)
           ,@width decimal(18,0)
           ,@height  decimal(18,0)
           ,@changed_by nvarchar(max)
           ,@container_type int = NULL		-- 0 = NotSet, 1 = Not Specified, 2 = PreCount, 3 = PrePackagedOrder, 4 = OverPack
	   ,@description nvarchar(max) = NULL
           ,@changed_on datetime = NULL
		   )
AS
BEGIN
	SET NOCOUNT ON;

	if @container_type IS NULL
	BEGIN
		SET @container_type = 3	-- If nothing specified, then make this a PPO container
	END

	-- Make sure we don't set an invalid shape
	if @shape > 2 
	BEGIN
		SET @shape = 2
	END

	DECLARE @ContainerTypeName nvarchar(80)
	SELECT @ContainerTypeName =
		CASE @container_type
		WHEN 1 THEN 'Not Specified'
		WHEN 2 THEN 'PreCount'
