            p.medcenter_id AS MedCenterId, 
			CASE WHEN p.package_status = 1 THEN MIN(p.[expiry_date]) ELSE NULL END AS EarliestPackageExpiry, 
			COUNT(*) AS TotalCount, 
			p.package_status AS PackageStatus, 
			ISNULL(PackageReservations.ReservedCount, 0) AS ReservedCount
FROM        dbo.package AS p 
			INNER JOIN dbo.package_definition ON p.package_definition_id = dbo.package_definition.package_definition_id 
