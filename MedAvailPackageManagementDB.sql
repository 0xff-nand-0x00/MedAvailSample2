  DECLARE @height int = 77
  DECLARE @changed_by nvarchar(max) = 'fred'
  EXEC ModifyContainer @container_id,@shape,@length,@width,@height,@changed_by
*/
CREATE PROCEDURE [dbo].[ModifyContainer]
(
           @container_id nvarchar(255)
           ,@shape int
           ,@length decimal(18,0)
           ,@width decimal(18,0)
           ,@height  decimal(18,0)
           ,@changed_by nvarchar(max)
           ,@container_type int = NULL		-- 0 = NotSet, 1 = Not Specified, 2 = PreCount, 3 = PrePackagedOrder, 4 = OverPack
           ,@description nvarchar(max) = NULL
           ,@changed_on datetime = NULL
)
AS
BEGIN
	SET NOCOUNT ON;

	if @container_type IS NULL
