CREATE FUNCTION [dbo].[uf_GetTableColumnNames]
(
	@table_name SYSNAME
)
RETURNS NVARCHAR(4000)
BEGIN


	DECLARE @ID INT
	DECLARE @ColumnNames NVARCHAR(4000)


	SELECT @ID = id
	FROM sys.sysobjects
	WHERE name = @table_name

	SET @ColumnNames = ',';
	SELECT @ColumnNames = @ColumnNames + ',' + name 
	FROM sys.syscolumns
	WHERE id = @ID
	SET @ColumnNames = REPLACE(@ColumnNames,',,','') 

	RETURN @ColumnNames
END
