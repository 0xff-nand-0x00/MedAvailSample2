EXEC dbo.[PermanentlyDeleteAllPackagesForMedCenter] @MedCenterSerialNumber ='M5-00001', @Debug = 1   --- Verbose 


***********************************************************************************************************************/
CREATE PROCEDURE [dbo].[PermanentlyDeleteAllPackagesForMedCenter]
/*
	Purpose of this stored proc is to purge all packages that are marked as 'Removed' from package table for a given medcenter serialnumber
*/
	@MedCenterSerialNumber	VARCHAR(100),
	@Debug					BIT = 0  -- Used for debugging purposes
AS
BEGIN
	
	declare @trancount INT;
	SET @trancount = @@TRANCOUNT
	
	SET NOCOUNT ON;
	
	BEGIN TRY
	
		

		if EXISTS ( 
			SELECT	NULL 
			FROM	[dbo].package p
			WHERE	P.MedCenter_ID = @MedCenterSerialNumber
			AND		(P.package_status < 5 OR P.Package_status = 5 )
		)
		BEGIN 

			if @trancount = 0
				begin transaction
			else
				save transaction T1;

			IF @Debug = 1
			BEGIN 
				PRINT CONVERT(varchar(50),GETDATE() ) + ' : Mark all packages as ''Removed'' for MedCenter: ' + @MedCenterSerialNumber;
			END
		
			UPDATE package
			SET package_status = 5
			FROM [dbo].package p
			WHERE P.MedCenter_ID = @MedCenterSerialNumber
			AND  package_status < 5;

			IF @Debug = 1
			BEGIN 
				PRINT CONVERT(varchar(50),GETDATE() ) + ' : Remove all package Images marked as ''Removed'' for MedCenter: ' + @MedCenterSerialNumber;
			END

			DELETE PI 
			FROM dbo.PackageImage PI
			INNER JOIN dbo.Package P ON PI.Package_ID = P.Package_ID
			WHERE P.MedCenter_ID = @MedCenterSerialNumber
			AND P.Package_Status = 5

			IF @Debug = 1
			BEGIN 
				PRINT CONVERT(varchar(50),GETDATE() ) + ' : Remove all packages marked as ''Removed'' for MedCenter: ' + @MedCenterSerialNumber;
			END
		
			DELETE	p	
			FROM	[dbo].package p
			WHERE	P.medcenter_id = @MedCenterSerialNumber
			AND 	P.package_status = 5;

			if @trancount = 0
				commit;
			IF @Debug = 1
			BEGIN 
				PRINT CONVERT(varchar(50),GETDATE() ) + ' : Execution of PermanentlyDeleteAllPackagesForMedCenter for MedCenterSerialNumber: ' + @MedCenterSerialNumber + ' completed!'
			END 
		END
		ELSE
		BEGIN 
			IF @Debug = 1
			BEGIN 
				PRINT CONVERT(varchar(50),GETDATE() ) + ' : No eligible records or database is already empty; MedCenter: ' + @MedCenterSerialNumber;
			END
		END

	
	END TRY
	BEGIN CATCH
			SELECT 
			ERROR_NUMBER() AS ErrorNumber,
			ERROR_SEVERITY() AS ErrorSeverity,
			ERROR_STATE() as ErrorState,
			ERROR_PROCEDURE() as ErrorProcedure,
			ERROR_LINE() as ErrorLine,
			ERROR_MESSAGE() as ErrorMessage;

			declare @XSTATE INT;
			select @XSTATE = XACT_STATE();
			-- Test whether the transaction is uncommittable.
		if @XSTATE = -1
			rollback;
        if @XSTATE = 1 and @trancount = 0
            rollback
        if @XSTATE = 1 and @trancount > 0
            rollback transaction T1;
		IF @Debug = 1
		BEGIN 
			PRINT CONVERT(varchar(50),GETDATE() ) + N' :
				The transaction is in an uncommittable state. ' +
				'Rolling back transaction.'
		END
		

	END CATCH
END

