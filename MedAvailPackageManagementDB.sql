-- =============================================
-- Author:		Paul Stauskas
-- Create date: July 24, 2018
-- Description:	Copies tables from any schema into new DataAcuisition database
-- Sample Call: EXEC [CopyTables] 'MedAvailDataAcquisitionDb', TableNames TYPE containing table names
-- =============================================
CREATE PROCEDURE [dbo].[CopyTables]
	@DatabaseName varchar(200),
	@TableVariable AS TableNames READONLY
AS
BEGIN
	-- SET NOCOUNT ON added to prevent extra result sets from
	-- interfering with SELECT statements.
	SET NOCOUNT ON;

	SET @DatabaseName = @DatabaseName + '.dbo.'
	DECLARE @script nvarchar(4000)
	DECLARE @dummy nvarchar(4000)
	DECLARE @TableName nvarchar(200)


	BEGIN TRY
		BEGIN TRANSACTION

			DECLARE TableNameCursor CURSOR FOR
				SELECT * FROM @TableVariable

			OPEN TableNameCursor
			FETCH NEXT FROM TableNameCursor INTO @TableName

			WHILE @@FETCH_STATUS = 0
			BEGIN
				SET @script = 'DECLARE @script nvarchar(4000) '
				SET @script = @script + ' SET @script = ' + @DatabaseName + 'uf_CreateInsertScript(''' + @TableName + ''',''' + @databasename + ''')'
				SET @script = @script + ' EXEC(@script)'

				--PRINT @script

				EXEC(@script)

				FETCH NEXT FROM TableNameCursor INTO @TableName
			END

			CLOSE TableNameCursor
			DEALLOCATE TableNameCursor

		COMMIT
	END TRY
	BEGIN CATCH
		ROLLBACK
	END CATCH

END
GO
