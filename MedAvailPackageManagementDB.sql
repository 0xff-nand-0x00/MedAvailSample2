-- =============================================
-- Author:		Paul Stauskas
-- Create date: July 30, 2018
-- Description:	Creates Wrapper methods for the named function or stored proc
-- Usage:       print dbo.[CreateDBCallWrapper]('CreateInsertScript','dbo')
-- =============================================
CREATE FUNCTION [dbo].[CreateDBCallWrapper]( @ObjectName SYSNAME, @SchemaName SYSNAME)
RETURNS VARCHAR(MAX)
AS
BEGIN

	DECLARE @ReturnTypeDeclaration VARCHAR(MAX)
	set @ReturnTypeDeclaration = 'public virtual IEnumerable<T> ' + @ObjectName +  '<T>(' 


	DECLARE @DynamicParameters VARCHAR(MAX)
	SET @DynamicParameters = ''

	DECLARE @NormalDeclaration VARCHAR(MAX)
	set @NormalDeclaration = 'public virtual dynamic ' + @ObjectName +  '(' 

	DECLARE @ConstructorKeys VARCHAR(MAX)
	SET @ConstructorKeys = ''
	SELECT @ConstructorKeys = 
				CASE
					WHEN LEN(Procs.ParameterName) = 0 THEN ''
					ELSE
						@ConstructorKeys +
						CASE
							WHEN ParameterDataType LIKE '%CHAR%' THEN 'string '
							WHEN ParameterDataType LIKE '%BIGINT%' THEN 'long '
							WHEN ParameterDataType LIKE '%INT%' THEN 'int '
							WHEN ParameterDataType LIKE '%DATETIME%' THEN 'DateTime '
							WHEN ParameterDataType LIKE '%BINARY%' THEN 'byte[] '
							WHEN ParameterDataType = 'BIT' THEN 'bool '
							WHEN ParameterDataType LIKE '%TEXT%' THEN 'string '
							WHEN ParameterDataType LIKE '%SYSNAME%' THEN 'string '
							WHEN ParameterDataType LIKE '%DECIMAL%' THEN 'Decimal '   
							WHEN ParameterDataType LIKE '%MONEY%' THEN 'Decimal '   
							WHEN ParameterDataType LIKE '%FLOAT%' THEN 'Double '  
							WHEN ParameterDataType LIKE '%UNIQ%' THEN 'Guid '
							ELSE 'object '
						END + Procs.ParameterName + ','

				END 


		,@DynamicParameters = 
				CASE
					WHEN LEN(Procs.ParameterName) = 0 THEN ''
					ELSE
						@DynamicParameters + 'parameters.Add("' + Procs.ParameterName + '",' +  Procs.ParameterName + ');' +  + CHAR(13) + CHAR(9)
				END 
	FROM (SELECT SCHEMA_NAME(SCHEMA_ID) AS [Schema], 
		SO.name AS [ObjectName],
		SO.Type_Desc AS [ObjectType (UDF/SP)],
		P.parameter_id AS [ParameterID],
		P.name AS [ParameterName],
		TYPE_NAME(P.user_type_id) AS [ParameterDataType],
		P.max_length AS [ParameterMaxBytes],
		P.is_output AS [IsOutPutParameter]
		FROM sys.objects AS SO
		INNER JOIN sys.parameters AS P 
		ON SO.OBJECT_ID = P.OBJECT_ID
		WHERE SO.OBJECT_ID IN ( SELECT OBJECT_ID 
		FROM sys.objects
		WHERE TYPE IN ('P','FN'))
		) as Procs
	WHERE ObjectName = @ObjectName
	AND [Schema] = @SchemaName 
	ORDER BY [Schema], [ObjectName], [ParameterID]


	-- Trim off the trailing comma
	DECLARE @ConstructorKeyLen int
	SET @ConstructorKeyLen = LEN(@ConstructorKeys) - 1;
	if(@ConstructorKeyLen > 0)
	BEGIN
		SET @ConstructorKeys = SUBSTRING(@ConstructorKeys,1,@ConstructorKeyLen);
	END

	-- If there are no parameters, then delete the stub code generated
	if(@ConstructorKeys = 'string ')
	BEGIN
		SET @ConstructorKeys = ''
		SET @DynamicParameters = ''
	END

	SET @DynamicParameters = REPLACE(@DynamicParameters, 'parameters.Add("",);','');


	set @NormalDeclaration = @NormalDeclaration + @ConstructorKeys + ')' + CHAR(13) + CHAR(10) + '{'+ CHAR(13) + CHAR(10) + CHAR(9) + 'var parameters = new DynamicParameters();' + CHAR(13) + CHAR(10)
	set @NormalDeclaration = @NormalDeclaration + CHAR(9) + @DynamicParameters + CHAR(13) + CHAR(10)
	set @NormalDeclaration = @NormalDeclaration + CHAR(9) + 'return(databaseContext.ExecuteNoAudit("' + @ObjectName + '", parameters, null, CommandType.StoredProcedure));'  +  CHAR(13) + CHAR(10)
	set @NormalDeclaration = @NormalDeclaration  + '}' + CHAR(13) + CHAR(10)

	set @ReturnTypeDeclaration = @ReturnTypeDeclaration + @ConstructorKeys + ')' + CHAR(13) + CHAR(10) + '{'+ CHAR(13) + CHAR(9) + 'var parameters = new DynamicParameters();' + CHAR(13)+ CHAR(10)
	set @ReturnTypeDeclaration = @ReturnTypeDeclaration + CHAR(9) + @DynamicParameters + CHAR(13)+ CHAR(10)
	set @ReturnTypeDeclaration = @ReturnTypeDeclaration + CHAR(9) + 'IEnumerable<T> retVal = databaseContext.QueryNoAudit<T>("' + @ObjectName + '", parameters, null, CommandType.StoredProcedure);'  +  CHAR(13) + CHAR(10)
	set @ReturnTypeDeclaration = @ReturnTypeDeclaration  + CHAR(9) + 'return(retVal);' + CHAR(13) + CHAR(10)+ '}'+ CHAR(10) + CHAR(13) 

	set @NormalDeclaration = @NormalDeclaration + CHAR(13) + CHAR(10) + @ReturnTypeDeclaration

	RETURN @NormalDeclaration
END
