	SELECT @ContainerTypeName =
		CASE @container_type
		WHEN 1 THEN 'Not Specified'
		WHEN 2 THEN 'PreCount'
		WHEN 3 THEN 'PPO'
		WHEN 4 THEN 'OverPack'
		ELSE 'Not Set'
	END


	if @changed_on IS NULL
	BEGIN
		SET @changed_on = GETDATE();
	END

	UPDATE [dbo].[container]
	SET [description] = @description
			   ,[shape] = @shape
			   ,[length] = @length
			   ,[width] = @width
			   ,[height] = @height
			   ,[container_type] = @container_type
			   ,[changed_by] = @changed_by
