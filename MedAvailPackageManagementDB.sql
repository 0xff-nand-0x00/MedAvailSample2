			dbo.package_definition.product_code AS ProductCode, 
            dbo.package_definition.product_code_type_id AS ProductCodeType, 
			dbo.package_definition.package_size AS PackageSize, 
			dbo.package_definition.package_size_uom_id AS PackageSizeUOM, 
