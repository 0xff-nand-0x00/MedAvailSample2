--- =============================================
-- Author:	Paul Stauskas
-- Create date: Aug 13 30, 2018
-- Description:	Generates a C# POCO class that wraps the select statement given.
--              In other words its a DTO that matches your query results exactly.
-- Usage:       Output to text
--		EXEC [dbo].[GenerateDTOFromSelect] 'package_id,medcenter_id', 'select * from package', 'MyPartsDTO'
-- =============================================
CREATE PROCEDURE [dbo].[GenerateDTOFromSelect] @ColumnNames as nvarchar(400), @SelectStatement as nvarchar(max), @table_name SYSNAME
AS
BEGIN
	DECLARE @temp_table_name nvarchar(100)
	SET @temp_table_name = '##DTO_' +  REPLACE( CAST(NEWID() as nvarchar(100)),'-','')

	DECLARE @sql nvarchar(1000)

	set @sql = 'IF OBJECT_ID(''tempdb..' + @temp_table_name + ''') IS NOT NULL DROP Table ' + @temp_table_name
	set @sql = 'SELECT ' + @ColumnNames  + ' INTO ' + @temp_table_name + ' FROM (' + @SelectStatement + ') as SubQ1 WHERE 0=1'

	EXEC (@sql)

	SELECT Code FROM dbo.[usp_GenerateDTOFromSelect](@table_name,@temp_table_name) ORDER BY SORT ASC

	-- Make sure we drop the temp table used to generate the shema for the class
	set @sql = ' DROP Table ' + @temp_table_name
	EXEC (@sql)
END
