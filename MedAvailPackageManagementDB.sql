			(
				SELECT PackageCode, medcenter_id, count(PackageCode) AS ReservedCount
				FROM dbo.PackageReservation
				GROUP BY PackageCode, medcenter_id, Deleted
				HAVING Deleted = 0
			) AS PackageReservations ON PackageReservations.medcenter_id = p.medcenter_id 
				AND PackageReservations.PackageCode = dbo.package_definition.package_code 
				AND p.package_status = 1
WHERE       (p.package_status <> 5)
GROUP BY 	
			dbo.package_definition.package_code, 
			dbo.package_definition.product_code, 
