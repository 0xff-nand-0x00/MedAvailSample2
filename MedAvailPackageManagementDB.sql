-- =============================================
-- Author:		Paul Stauskas
-- Create date: Sept 3, 2018
-- Description:	Determines if the specified table has been enabled for auditing. Returns 1 for yes, 0 for no.
-- Sample Call: DECLARE @IsAuditable bit
--		SELECT @IsAuditable = dbo.IsAuditable('junk') 
-- =============================================
CREATE FUNCTION [dbo].[IsAuditable](@TableName nvarchar(80))
RETURNS bit
AS BEGIN
	DECLARE @IsAuditable bit

	SELECT @IsAuditable = 
	CASE WHEN EXISTS( SELECT 1 from INFORMATION_SCHEMA.columns
               where
                     table_name = @TableName 
                     and column_name = 'AuditID_MA'
		) THEN 1
		ELSE 0
	END 

	return @IsAuditable
END
